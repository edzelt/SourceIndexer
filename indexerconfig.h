// === IndexerConfig.h ===
#pragma once

#include <QString>
#include <QStringList>

// Конфигурация индексатора.
// Хранится в indexer_config.json рядом с исполняемым файлом.
// Все поля имеют разумные значения по умолчанию —
// при первом запуске файл конфигурации создаётся автоматически.

struct IndexerConfig {

    // ── Паттерны пропуска файлов ──────────────────────────────────────────
    // Файл пропускается если его имя (без пути) совпадает с одним из паттернов.
    // Поддерживаются wildcards: * и ?  (используется QRegularExpression через
    // QDir::match, т.е. * = любая строка без разделителя пути).
    // Примеры: "moc_*", "*.rc", "cmake_pch.*", "qrc_*"
    QStringList skipFilePatterns;

    // ── Паттерны пропуска директорий ──────────────────────────────────────
    // Файл пропускается если его полный путь содержит один из этих подстрок.
    // Примеры: "/build/", "/out/", "/.git/"
    QStringList skipDirPatterns;

    // ── Дополнительные include-пути ───────────────────────────────────────
    // Добавляются как -I к аргументам каждого файла.
    // Помогает исправить "file not found" для внешних зависимостей
    // которые не попали в compile_commands.json.
    QStringList extraIncludePaths;

    // ── Дополнительные defines ────────────────────────────────────────────
    // Добавляются как -D к аргументам каждого файла.
    // Примеры: "QT_NO_KEYWORDS", "MYPROJECT_STATIC"
    QStringList extraDefines;

    // ── Подавление диагностик в логе ──────────────────────────────────────
    // Диагностика libclang не попадает в лог если её текст содержит
    // одну из этих подстрок (регистронезависимо).
    // Используется для заведомо известных и неустранимых предупреждений —
    // например "*.moc' file not found" (Qt генерирует эти файлы в папке
    // сборки, при индексации из исходников их нет, это нормально).
    // Примеры: ".moc' file not found", "unknown argument"
    QStringList suppressDiagPatterns;

    // ── Загрузка / сохранение ─────────────────────────────────────────────

    // Загружает конфигурацию из файла. Если файл не существует —
    // заполняет поля значениями по умолчанию и сохраняет файл.
    static IndexerConfig load();

    // Сохраняет конфигурацию в файл.
    void save() const;

    // Путь к файлу конфигурации (рядом с исполняемым файлом)
    static QString configPath();

    // Значения по умолчанию
    static IndexerConfig defaults();
};